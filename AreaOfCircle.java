package com.company;

//WAP in Java to Find Area Of Circle

public class AreaOfCircle {
    public static void main(String[] args) {
        // Declare and initialize radius and PI value of circle
        final float PI = 3.14f; // Declare and initialize the PI value as final because the value of PI is always the same
        float radiusOfCircle = 4.5f;
        float area = PI * radiusOfCircle * radiusOfCircle;

        //Calculate of area of circle

        System.out.println("Area of Circle is " + area);

    }
}
